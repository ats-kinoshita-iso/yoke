"""Model comparison eval — compare contextual summary quality across models.

Takes 10 chunks from the ingested corpus and generates contextual summaries
using three models (Sonnet, Haiku, Gemma-4). An LLM judge scores each summary
on accuracy, contextual value, and conciseness. Also tracks latency and cost.

Run: uv run python -m pytest evals/model_comparison.py -v -s
"""

import io
import json
import sys
import time
from pathlib import Path

import httpx
import pytest
from dotenv import load_dotenv
from pydantic import BaseModel, Field

from evals._judge import generate as llm_generate
from evals._judge import judge
from yoke.ingestion.chunking import chunk_text
from yoke.ingestion.enrichment import (
    ENRICHMENT_PROMPT,
    ENRICHMENT_SYSTEM,
    _clean_summary,
    _get_doc_outline,
)
from yoke.ingestion.models import Chunk

# Fix Windows console encoding
if sys.stdout.encoding != "utf-8":
    sys.stdout = io.TextIOWrapper(
        sys.stdout.buffer, encoding="utf-8", errors="replace"
    )

load_dotenv(override=True)

FIXTURE_PATH = (
    Path(__file__).resolve().parent.parent
    / "tests" / "fixtures" / "docs-math" / "lee_ch1.txt"
)
RESULTS_DIR = Path(__file__).resolve().parent / "results"

# ---------------------------------------------------------------------------
# Models under test
# ---------------------------------------------------------------------------

MODELS = [
    {"provider": "claude", "model": "claude-sonnet-4-20250514", "label": "Sonnet"},
    {"provider": "claude", "model": "claude-haiku-4-5-20251001", "label": "Haiku"},
    {"provider": "ollama", "model": "gemma4:e4b", "label": "Gemma-4 (local)"},
]

# If a model exceeds this time on its first chunk, skip remaining chunks
_SLOW_MODEL_THRESHOLD_S = 120.0

# Cost per 1K tokens (input/output) for API models — approximate
_COST_PER_1K: dict[str, tuple[float, float]] = {
    "claude-sonnet-4-20250514": (0.003, 0.015),
    "claude-haiku-4-5-20251001": (0.0008, 0.004),
}

# ---------------------------------------------------------------------------
# Summary generation
# ---------------------------------------------------------------------------


def _estimate_tokens(text: str) -> int:
    return len(text) // 4


def _generate_summary(
    provider: str,
    model: str,
    prompt: str,
) -> tuple[str, float]:
    """Generate a summary and return (cleaned_summary, elapsed_seconds)."""
    model_spec = f"{provider}/{model}" if provider != "claude" else model
    start = time.perf_counter()
    raw = llm_generate(model_spec, prompt, system=ENRICHMENT_SYSTEM)
    elapsed = time.perf_counter() - start
    return _clean_summary(raw), elapsed


def _estimate_cost(
    model: str,
    prompt: str,
    summary: str,
) -> float | None:
    """Estimate USD cost for an API call, or None for local models."""
    rates = _COST_PER_1K.get(model)
    if rates is None:
        return None
    input_cost, output_cost = rates
    input_tokens = _estimate_tokens(prompt) / 1000
    output_tokens = _estimate_tokens(summary) / 1000
    return input_tokens * input_cost + output_tokens * output_cost


# ---------------------------------------------------------------------------
# Judge
# ---------------------------------------------------------------------------

JUDGE_SYSTEM = (
    "You are an evaluation judge for contextual chunk summaries in a knowledge "
    "management system. You will be given the original chunk text and a 3-sentence "
    "contextual summary generated by a model.\n\n"
    "Score the summary using the score_summary tool. Be strict but fair.\n\n"
    "Accuracy (1-5): Does the summary correctly describe the chunk's content? "
    "5 = every claim is factually correct and matches the chunk. "
    "1 = contains fabricated or incorrect information.\n\n"
    "Contextual value (1-5): Does the summary add useful context beyond what "
    "the chunk says? 5 = provides helpful positioning (what comes before/after, "
    "how it fits the broader document). 1 = just restates the chunk.\n\n"
    "Conciseness (1-5): Is the summary appropriately brief? "
    "5 = exactly 3 concise sentences, no fluff. "
    "3 = reasonable length but has unnecessary words. "
    "1 = excessively long or rambling."
)

JUDGE_TOOL = {
    "name": "score_summary",
    "description": "Score a contextual summary on accuracy, contextual value, and conciseness.",
    "input_schema": {
        "type": "object",
        "properties": {
            "accuracy": {
                "type": "integer",
                "description": "1-5. Does the summary correctly describe the chunk?",
            },
            "contextual_value": {
                "type": "integer",
                "description": "1-5. Does it add useful context beyond the chunk?",
            },
            "conciseness": {
                "type": "integer",
                "description": "1-5. Is it appropriately brief (3 sentences)?",
            },
            "reasoning": {
                "type": "string",
                "description": "Brief explanation of the scores.",
            },
        },
        "required": ["accuracy", "contextual_value", "conciseness", "reasoning"],
    },
}


class JudgeScore(BaseModel):
    accuracy: int = Field(ge=1, le=5)
    contextual_value: int = Field(ge=1, le=5)
    conciseness: int = Field(ge=1, le=5)
    reasoning: str


# ---------------------------------------------------------------------------
# Fixtures
# ---------------------------------------------------------------------------


@pytest.fixture(scope="module")
def sample_chunks() -> tuple[list[Chunk], str]:
    """Load 10 evenly-spaced chunks from the fixture corpus."""
    full_text = FIXTURE_PATH.read_text(encoding="utf-8")
    all_chunks = chunk_text(full_text, source_file="lee_ch1.txt")

    # Pick 10 evenly spaced chunks to cover the full document
    n = len(all_chunks)
    step = max(1, n // 10)
    selected = all_chunks[:n:step][:10]

    return selected, full_text


# ---------------------------------------------------------------------------
# Eval
# ---------------------------------------------------------------------------


class TestModelComparison:
    """Compare contextual summary quality across models."""

    def test_compare_summary_models(
        self,
        sample_chunks: tuple[list[Chunk], str],
        judge_model: str,
    ) -> None:
        """Generate and judge summaries from each model."""
        chunks, full_text = sample_chunks
        doc_outline = _get_doc_outline(full_text)

        # model_label -> list of per-chunk results
        results: dict[str, list[dict]] = {m["label"]: [] for m in MODELS}
        skipped_models: set[str] = set()

        for i, chunk in enumerate(chunks):
            prompt = ENRICHMENT_PROMPT.format(
                doc_outline=doc_outline,
                chunk_text=chunk.text,
            )

            for model_spec in MODELS:
                label = model_spec["label"]
                if label in skipped_models:
                    continue

                # Generate summary
                try:
                    summary, elapsed = _generate_summary(
                        model_spec["provider"],
                        model_spec["model"],
                        prompt,
                    )
                except (
                    httpx.ConnectError,
                    httpx.HTTPStatusError,
                    httpx.ReadTimeout,
                ) as exc:
                    print(f"\n  SKIP {label}: {type(exc).__name__}: {exc}")
                    skipped_models.add(label)
                    continue

                # Skip models that are too slow after the first chunk
                if elapsed > _SLOW_MODEL_THRESHOLD_S and i == 0:
                    print(
                        f"\n  SKIP {label}: too slow ({elapsed:.0f}s > "
                        f"{_SLOW_MODEL_THRESHOLD_S:.0f}s threshold)"
                    )
                    skipped_models.add(label)
                    continue

                cost = _estimate_cost(model_spec["model"], prompt, summary)

                # Judge
                score = judge(
                    model=judge_model,
                    system=JUDGE_SYSTEM,
                    user_prompt=(
                        f"Chunk text:\n{chunk.text}\n\n"
                        f"Summary:\n{summary}"
                    ),
                    tool=JUDGE_TOOL,
                    score_cls=JudgeScore,
                )

                results[label].append({
                    "chunk_index": chunk.chunk_index,
                    "summary": summary,
                    "accuracy": score.accuracy,
                    "contextual_value": score.contextual_value,
                    "conciseness": score.conciseness,
                    "reasoning": score.reasoning,
                    "elapsed_s": round(elapsed, 2),
                    "cost_usd": round(cost, 6) if cost is not None else None,
                })

                print(
                    f"\n  chunk {i+1:>2}/10 | {label:>14s} | "
                    f"acc={score.accuracy} ctx={score.contextual_value} "
                    f"con={score.conciseness} | "
                    f"{elapsed:.1f}s"
                    f"{f' ${cost:.5f}' if cost is not None else ' (local)'}"
                )

        # --- Aggregate and print comparison table ---
        active_models = [m for m in MODELS if m["label"] not in skipped_models]
        if not active_models:
            pytest.fail("All models were skipped — cannot compare")

        print("\n")
        print("  Model Comparison — Contextual Summary Quality (10 chunks)")
        print("  " + "=" * 76)
        print(
            f"  {'Model':>14s}  {'Accuracy':>8s}  {'Context':>8s}  "
            f"{'Concise':>8s}  {'Avg':>6s}  {'Time/ch':>8s}  {'Cost/ch':>9s}"
        )
        print(f"  {'-' * 76}")

        summary_data: dict[str, dict] = {}
        for model_spec in active_models:
            label = model_spec["label"]
            rows = results[label]
            if not rows:
                continue

            n = len(rows)
            avg_acc = sum(r["accuracy"] for r in rows) / n
            avg_ctx = sum(r["contextual_value"] for r in rows) / n
            avg_con = sum(r["conciseness"] for r in rows) / n
            avg_all = (avg_acc + avg_ctx + avg_con) / 3
            avg_time = sum(r["elapsed_s"] for r in rows) / n

            costs = [r["cost_usd"] for r in rows if r["cost_usd"] is not None]
            avg_cost = sum(costs) / len(costs) if costs else None

            cost_str = f"${avg_cost:.5f}" if avg_cost is not None else "free"
            print(
                f"  {label:>14s}  {avg_acc:8.2f}  {avg_ctx:8.2f}  "
                f"{avg_con:8.2f}  {avg_all:6.2f}  {avg_time:7.1f}s  {cost_str:>9s}"
            )

            summary_data[label] = {
                "model": model_spec["model"],
                "provider": model_spec["provider"],
                "num_chunks": n,
                "avg_accuracy": round(avg_acc, 2),
                "avg_contextual_value": round(avg_ctx, 2),
                "avg_conciseness": round(avg_con, 2),
                "avg_overall": round(avg_all, 2),
                "avg_time_s": round(avg_time, 2),
                "avg_cost_usd": round(avg_cost, 6) if avg_cost is not None else None,
                "per_chunk_results": rows,
            }

        print("  " + "=" * 76)

        # --- Write results ---
        output = {
            "eval": "model_comparison",
            "corpus": "Lee, Introduction to Smooth Manifolds, Ch.1",
            "num_chunks": len(chunks),
            "judge_model": judge_model,
            "skipped_models": sorted(skipped_models),
            "models": summary_data,
        }
        RESULTS_DIR.mkdir(parents=True, exist_ok=True)
        results_path = RESULTS_DIR / "model_comparison.json"
        results_path.write_text(json.dumps(output, indent=2), encoding="utf-8")
        print(f"\n  Results written to {results_path}")

        # --- Assertions ---
        # At least 2 models must have run for a meaningful comparison
        assert len(summary_data) >= 2, (
            f"Only {len(summary_data)} model(s) produced results — need at least 2"
        )

        # All models should achieve at least 2.5 average accuracy
        for label, data in summary_data.items():
            assert data["avg_accuracy"] >= 2.5, (
                f"{label} avg accuracy {data['avg_accuracy']:.2f} below 2.5 threshold"
            )


